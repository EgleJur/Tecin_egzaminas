CREATE TABLE IF NOT EXISTS MEAL_TABLE
(
    MEAL_ID BIGINT GENERATED BY DEFAULT AS IDENTITY,
    NAME VARCHAR (255),
    DESCRIPTION VARCHAR (255),
    CONSTRAINT MEAL_PK PRIMARY KEY(MEAL_ID)
    );

CREATE TABLE IF NOT EXISTS ORDERING_MEALS
(
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY,
    MEAL_ID BIGINT,
    ORDERING_ID BIGINT,
    QUANTITY INTEGER
--    CONSTRAINT ORDERING_MEALS_PK PRIMARY KEY(ID)
);
CREATE TABLE IF NOT EXISTS MEALS_IN_MENU
(
    MEAL_ID BIGINT,
    MENU_ID BIGINT
);

